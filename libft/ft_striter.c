#include "libft.h"

void	ft_striter(char *s, void (*f)(char *))
{
	int		i;

	if ((s != 0) && (f != 0))
	{
		i = 0;
		while (s[i] != '\0')
		{
			(*f)(&(s[i]));
			i++;
		}
	}
}
