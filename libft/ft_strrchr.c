#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int		i;

	i = 0;
	while (c < 0)
		c = c + 256;
	while (s[i] != '\0')
		i++;
	while (i != -1)
	{
		if (s[i] == (c % 256))
			return (&(((char*)s)[i]));
		i--;
	}
	return (NULL);
}
