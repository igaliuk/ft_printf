#include "ft_printf.h"

void	ft_aaff(char **str)
{
	int		i;

	i = 0;
	while ((*str)[i])
	{
		if (((*str)[i] >= 'A') && ((*str)[i] <= 'F'))
			(*str)[i] = (*str)[i] - 'A' + 'a';
		i++;
	}
}
